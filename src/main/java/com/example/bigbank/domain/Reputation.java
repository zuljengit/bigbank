package com.example.bigbank.domain;

public class Reputation {

    private Number people;
    private Number state;
    private Number underworld;

    public Number getPeople() {
        return people;
    }

    public void setPeople(Number people) {
        this.people = people;
    }

    public Number getState() {
        return state;
    }

    public void setState(Number state) {
        this.state = state;
    }

    public Number getUnderworld() {
        return underworld;
    }

    public void setUnderworld(Number underworld) {
        this.underworld = underworld;
    }
}
